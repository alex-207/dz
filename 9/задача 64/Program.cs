ПРОГРАММА ВЫВВОДИТ ВСЕ НАТУРАЛЬНЫЕ ЧИСЛА В ПРОМЕЖУТКЕ ОТ N ДО 1, ЧЕРЕЗ РЕКУРСИЮ
=============================================================================================================*/
#pragma warning disable
string NumbersRec(int N)
{
    if (1 < N) return $"{N}, " + NumbersRec(N - 1);
    else return $"{N}";
}
Console.Write($"\"{NumbersRec(8)}\"");