/* 1. тсмйжхъ пейспяхбмни йнмбепрюжхх деяърхвмнцн вхякю б дбнхвмне
	  пейспемрмне яннрмньемхе:
	  декхрэ вхякн я нярюрйнл мю 2, онйю ме асдер днярхцмср 0 - сякнбхе бшундю хг пейспяхх
	  опх вермнл декемхх дбнхвмши нярюрнй пюбем 0, ю опх мевермнл декемхх дбнхвмши нярюрнй пюбем 1 */
void DecDub(int num) {
	if (num > 0) {   // БШУНД ХГ ПЕЙСПЯХХ
		DecDub(num / 2);  // ПЕЙСПЯХЪ 
	}
	printf("%d", num % 2);  // 010010011
}
