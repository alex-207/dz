/* 2. тсмйжхъ пейспяхбмнцн бнгбедемхъ вхякю б яреоемэ
	  пейспемрмне яннрмньемхе:
	  слмнфюрэ вхякн яюлн мю яеаъ, йюфдши пюг слемэьюъ яреоемэ мю 1 */
int LogNum(int num, int degree) {  // ВХЯКН, ЯРЕОЕМЭ
	if (degree > 0) {  // БШУНД ХГ ПЕЙЙСПЯХХ
		return LogNum(num, degree - 1) * num;  // ПЕЙСПЯХЪ
	}

	else {
		return 1;
	}
}
//=============================================================================================================
/* 3. тсмйжхъ пейспяхбмнцн бнгбедемхъ вхякю б яреоемэ, хяонкэгсъ ябниярбн вермнярх яреоемх
	  пейспемрмне яннрмньемхе:
	  еякх яреоемэ вермюъ: x^n = (x^2)^n/2 (ВХЯКН^2; ЯРЕОЕМЭ / 2)
				 мевермюъ: x^n = (x^2)^n-1 (ВХЯКН^2; ЯРЕОЕМЭ - 1 ) */
int PropDeg(int num, int degree) {

	if (degree > 0) {  // БШУНД ХГ ПЕЙЙСПЯХХ
		if (degree % 2 == 0) {  // ВЕРМЮЪ
			return LogNum(num * num, degree / 2);  // ПЕЙСПЯХЪ
		}

		else {  // МЕВЕРМЮЪ
			return LogNum(num * num, degree - 1);  // ПЕЙСПЯХЪ
		}
	}

	else {
		return 1;
	}
}
